<script>
import api from "@/common/api";
import Cherry from "cherry-markdown";
import { useUserStore } from "../../../public/stores/modules/user";
import { mapState } from "pinia";
export default {
  props: {
    userId: {
      type: String,
    },
  },
  computed: {
    ...mapState(useUserStore, ["userInfo"]),
  },
  mounted() {
    this.getTrainingList();
  },
  data() {
    return {
      rules: {
        title: [{ required: true, message: "请输入题单名称", trigger: "blur" }],
      },
      tainingCherry: null,
      trainingInfo: {
        title: "",
        description: "",
      },
      dialogVisible: false,
      trainingList: null,
      getTrainingListTwo: [],
      getTrainingListByCollect: [],
    };
  },
  methods: {
    addTraining() {
      let isValid = true;
      this.$refs["trainingInfo"].validate((valid) => {
        if (!valid) {
          isValid = false;
          return false;
        }
      });
      if (!isValid) {
        return;
      }

      this.trainingInfo.description = this.tainingCherry.getMarkdown();
      api.addTraining(this.trainingInfo).then((response) => {
        if (response.data.code == "200") {
          this.$message({
            message: "创建成功",
            type: "success",
          });
          this.dialogVisible = false;
          this.trainingInfo.description = "";
          this.trainingInfo.title = "";
          (this.getTrainingListTwo = []), (this.getTrainingListByCollect = []);
          this.getTrainingList();
        }
      });
    },
    initializeEditor() {
      this.$nextTick(() => {
        if (this.tainingCherry) {
          return;
        }
        this.tainingCherry = new Cherry({
          id: "markdownTraining",
          value: this.trainingInfo.description,
          themeSettings: {
            codeBlockTheme: "one dark",
          },
        });
      });
    },
    openDialog() {
      this.dialogVisible = true;
    },
    skipTraining(trainingId) {
      window.open(`/home/training/${trainingId}`, "_blank");
    },
    collect(data) {
      if (data.isCollect == true) {
        data.collectCount = data.collectCount - 1;
      } else {
        data.collectCount = data.collectCount + 1;
      }
      data.isCollect = !data.isCollect;
      api.addCollect({
        targetType: 2,
        targetId: data.trainingId,
        isCollect: data.isCollect,
      });
    },
    getTrainingList() {
      api.getTrainingList({ userId: this.userId }).then((response) => {
        this.trainingList = response.data.data;
        for (let i = 0; i < this.trainingList.length; i += 2) {
          if (this.trainingList[i + 1] == undefined) {
            this.getTrainingListTwo.push([this.trainingList[i]]);
          } else {
            this.getTrainingListTwo.push([
              this.trainingList[i],
              this.trainingList[i + 1],
            ]);
          }
        }
      });
      api.getTrainingListByCollect({ userId: this.userId }).then((response) => {
        this.trainingList = response.data.data;
        for (let i = 0; i < this.trainingList.length; i += 2) {
          if (this.trainingList[i + 1] == undefined) {
            this.getTrainingListByCollect.push([this.trainingList[i]]);
          } else {
            this.getTrainingListByCollect.push([
              this.trainingList[i],
              this.trainingList[i + 1],
            ]);
          }
        }
      });
    },
  },
};
